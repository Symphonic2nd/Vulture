public class Predator {
    
    public int x;
    public int y;
    
    public Predator(int xc, int yc) {
        x = xc;
        y = yc;
    }
    
    public void setCoord(int xc, int yc) {
        x = xc;
        y = yc;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
}
